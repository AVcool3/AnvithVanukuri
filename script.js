const byId = (id) => document.getElementById(id);

function createLink({ label, url }, className, text = label) {
  const link = document.createElement("a");
  link.className = className;
  link.href = url;
  link.textContent = text;

  if (!url.startsWith("mailto:")) {
    link.target = "_blank";
    link.rel = "noopener noreferrer";
  }

  return link;
}

function createTags(items) {
  const tags = document.createElement("ul");
  tags.className = "tag-list";

  items.forEach((item) => {
    const tag = document.createElement("li");
    tag.className = "tag";
    tag.textContent = item;
    tags.appendChild(tag);
  });

  return tags;
}

function renderSocialLinks() {
  const socialContainer = byId("social-links");
  const contactContainer = byId("contact-links");
  const actionsContainer = byId("hero-actions");

  profile.socials.forEach((social) => {
    const socialLink = createLink(social, "social-link", social.icon);
    socialLink.setAttribute("aria-label", social.label);
    socialContainer.appendChild(socialLink);

    const contactLink = createLink(social, "button", social.label);
    contactContainer.appendChild(contactLink);

    if (social.featured) {
      const action = createLink(social, social.primary ? "button primary" : "button");
      actionsContainer.appendChild(action);
    }
  });
}

function renderCourses() {
  const courseList = byId("course-list");

  profile.courses.forEach((course) => {
    const card = document.createElement("article");
    card.className = "card";
    card.innerHTML = `
      <h3>${course.title}</h3>
      <p>${course.description}</p>
    `;
    card.appendChild(createTags(course.skills));
    courseList.appendChild(card);
  });
}

function renderExperience() {
  const experienceList = byId("experience-list");

  profile.experiences.forEach((experience) => {
    const item = document.createElement("article");
    item.className = "timeline-item";

    const meta = [experience.period, experience.organization, experience.location]
      .filter(Boolean)
      .join(" · ");

    let body = `
      <p class="timeline-meta">${meta}</p>
      <h3>${experience.role}</h3>
    `;

    if (Array.isArray(experience.highlights) && experience.highlights.length) {
      const points = experience.highlights
        .map((point) => `<li>${point}</li>`)
        .join("");
      body += `<ul class="highlights">${points}</ul>`;
    } else if (experience.description) {
      body += `<p>${experience.description}</p>`;
    }

    item.innerHTML = body;
    experienceList.appendChild(item);
  });
}

function renderSkills() {
  const skillsContent = byId("skills-content");

  profile.skillGroups.forEach((group) => {
    const card = document.createElement("article");
    card.className = "card skill-group";
    card.innerHTML = `<h3>${group.title}</h3>`;
    card.appendChild(createTags(group.items));
    skillsContent.appendChild(card);
  });
}

function setupNavigation() {
  const toggle = document.querySelector(".menu-toggle");
  const navLinks = byId("nav-links");

  toggle.addEventListener("click", () => {
    const isOpen = navLinks.classList.toggle("is-open");
    toggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.addEventListener("click", (event) => {
    if (event.target.matches("a")) {
      navLinks.classList.remove("is-open");
      toggle.setAttribute("aria-expanded", "false");
    }
  });
}

function renderProfile() {
  byId("profile-name").textContent = profile.name;
  byId("profile-summary").textContent = profile.summary;
  byId("about-text").textContent = profile.about;
  byId("current-year").textContent = new Date().getFullYear();

  const photo = byId("profile-photo");
  photo.src = profile.photo;
  photo.alt = `Profile photo of ${profile.name}`;

  const educationList = byId("education-details");
  if (educationList && profile.education) {
    const { school, degree, period, location, details } = profile.education;
    const rows = [
      `<li><strong>${school}</strong>${location ? ` · ${location}` : ""}</li>`,
      `<li>${degree}</li>`,
      `<li>${period}</li>`,
      details ? `<li>${details}</li>` : "",
    ]
      .filter(Boolean)
      .join("");
    educationList.innerHTML = rows;
  }
}

